## Prerequisites

To use this application, ensure the following:

1. [Ollama](https://ollama.com) is installed.
2. At least one model is downloaded.

Refer to the [Ollama documentation](https://github.com/ollama/ollama) for guidance.

---

## How to Use

### Starting a New Chat

1. Click on the **New Chat** button in the sidebar.
2. Type your message in the input box at the bottom of the screen.
3. Press the **Send** button (arrow icon) to send your message.

### Accessing Recent Chats

1. Recent chats are listed under the **Recent Chats** section in the sidebar.
2. Click on a chat to load and continue the conversation.
3. The selected chat will be highlighted.

### Customizing Settings

1. Click on the **Settings** button (gear icon) in the sidebar or header.
2. In the settings modal:
- Select your preferred AI model from the dropdown menu.
- Enter a custom agent prompt in the text area.
3. Your changes will apply immediately.

### Clearing a Chat

1. To clear the current chat, click on the **Clear Chat** button (x icon) in the header or sidebar.

---

## Tips

- The chat interface automatically scrolls to the latest message for your convenience.
- If the AI is processing your input, a "Thinking..." indicator will appear.

